    === ARRAY ===
    
    Um array é um conjunto de elementos contidos numa mesma estrutura onde é possível definir um chave para cada elemento.
    
    É importante perceber que o conceito de Array em PHP é diferente do conceito clássico de Array.
    
    1. Os arrays não possuem tipo de dado
    2. É possível adicionar, alterar e remover elementos
    
    Podemos dizer que existem 3 abordagens, ou então, formas de trabalhar com Arrays.
    
    Array Indexados (numéricos) - estrutura onde os elementos possuem índeice numérico não negativo (primeiro elemento = 0.
    
    Array Associativos (mapas) - estrutura onde cada elemento está vinculado a uma chave, seja uma string ou um número
    
    Arrays Multidimensionais - estrutura que contém arrays como elementos

